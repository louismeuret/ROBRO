  /* Open-loop sinusoidal spin of a BLDC/PMSM through the STGIPN3H60T IPM.
   *   PA8/PB13  = HIN1/LIN1 -> phase U
   *   PA9/PB14  = HIN2/LIN2 -> phase V
   *   PA10/PB15 = HIN3/LIN3 -> phase W
   * TIM1 produces the complementary HIN/LIN pairs with dead-time; the IPM
   * inputs are active-high. No sensor, no current loop. */

  /* Hold PB0 low at all times. It defaults to ADC1_IN15 (analog); override it
   * to a push-pull output and drive it low. Set ODR low first to avoid a
   * glitch when the pin switches to output. */

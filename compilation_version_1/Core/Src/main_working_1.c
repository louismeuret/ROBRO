/* Apply a 3-phase sinusoidal voltage set for electrical angle theta_e [rad]
 * and modulation index m. Duty is centered at 50% so m=0 -> equal duties ->
 * zero differential voltage -> no winding current. */
static void motor_set_phase(float theta_e, float m)
{
  const float half = (float)PWM_ARR * 0.1f;
  float a = m * cosf(theta_e);
  float b = m * cosf(theta_e - TWO_PI / 3.0f);
  float c = m * cosf(theta_e + TWO_PI / 3.0f);

  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (uint32_t)(half * (1.0f + a)));
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, (uint32_t)(half * (1.0f + b)));
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, (uint32_t)(half * (1.0f + c)));
}


/* ----- Open-loop sinusoidal spin parameters (STGIPN3H60T 3-phase IPM) ----- */
/* TIM1 timer clock = 16 MHz, center-aligned: f_pwm = 16e6 / (2 * PWM_ARR).
 * PWM_ARR = 400 -> 20 kHz PWM. (The CubeMX default of 65535 gives ~122 Hz,
 * which is a scope smoke-test value, far too slow to drive a motor.) */
#define PWM_ARR            400u

/* Motor electrical/mechanical relationship. HC-KFS-class servos are 8-pole
 * (4 pole-pairs). CONFIRM against your motor's nameplate / datasheet.
 * Only affects the elec<->mech speed ratio, not whether the motor spins. */
#define POLE_PAIRS         4

/* Voltage modulation index, 0..~0.9 (fraction of DC bus applied as the sine
 * amplitude). Start LOW for first bring-up, then raise once direction and
 * smoothness look right on the scope / by feel. */
#define SPIN_MODULATION    0.10f

/* Scope demo: step the modulation index up by MOD_STEP once per second so the
 * duty-cycle swing grows visibly, then wrap back to SPIN_MODULATION once it
 * passes MOD_MAX so the pattern repeats. */
#define MOD_STEP           0.05f
#define MOD_MAX            0.45f

/* Target electrical frequency [Hz] and the time [s] to ramp up to it from 0.
 * Mechanical speed = SPIN_ELEC_HZ * 60 / POLE_PAIRS  (e.g. 5 Hz, 4 pp -> 75 rpm).
 * Open-loop has no feedback, so keep the speed low and the ramp gentle. */
#define SPIN_ELEC_HZ       5.0f
#define SPIN_RAMP_S        2.0f

#define TWO_PI             6.28318530718f

